    private void ButtonTitularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonTitularActionPerformed
        // TODO add your handling code here:
        listaAux = lista.filtrarTipo("titular");
        setJTextField(listaAux);
        
        ButtonAnterior.setEnabled(false);
        
        if(listaAux.vacia())
        {
            ButtonSiguiente.setEnabled(false);
            JFrame frame = new JFrame();
            JOptionPane.showMessageDialog(frame, "AVISO. No hay ning√∫n jugador registrado de ese tipo.");
        }
        else if(listaAux.primero().getSgt() == null)
            ButtonSiguiente.setEnabled(false);
        else
            ButtonSiguiente.setEnabled(true);
    }//GEN-LAST:event_ButtonTitularActionPerformed

