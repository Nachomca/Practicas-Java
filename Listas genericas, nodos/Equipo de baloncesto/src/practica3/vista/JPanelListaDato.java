    public void iniBotones()
    {
        ButtonAnterior.setEnabled(false);
        
        if(lista.vacia())
        {
            ButtonSiguiente.setEnabled(false);
            JFrame frame = new JFrame();
            JOptionPane.showMessageDialog(frame, "AVISO. No hay ning√∫n jugador registrado.");
        }
        else
        {
            ButtonSiguiente.setEnabled(false);
            JFrame frame = new JFrame();
            JOptionPane.showMessageDialog(frame, "Selecciona antes el metodo de filtrado.");
        }
        
    }
    
