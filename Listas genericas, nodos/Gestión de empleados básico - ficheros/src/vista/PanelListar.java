    public void setJTextField(Lista <Empleado> p){
        String cadena = p.extraerObjeto().toString();     
        String [] datos = cadena.split("\\*");
        jTextTipo.setText(datos[0]);
        jTextNumero.setText(datos[1]);
        jTFNombre.setText(datos[2]);
        jTFSueldo.setText(datos[3]);
        jTFFecha.setText(datos[4]);
        jTextPagas.setText(datos[5]);
        jTextAtributo.setText(datos[6]);

    }
    
