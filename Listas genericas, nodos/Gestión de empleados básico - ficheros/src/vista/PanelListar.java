    private void BotCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotCalcularActionPerformed
        // TODO add your handling code here:
        if(jTextTipo.getText().equals("Analista")){
            //System.out.println("Entra en analista");
            Analista ana = (Analista)list.extraerObjeto();
            
            if(ana.cumpleAño())
            {
                float Sueldo,Resultado;
                Sueldo = ana.getSueldo();
                
                Resultado = Sueldo + Sueldo * ana.getPlusAnual();
                
                jTFSueldo.setText(Float.toString(Resultado));
            }else
            {
                JFrame frame = new JFrame();
                JOptionPane.showMessageDialog(frame, "Error. El Empleado no cumple el requisito de 1 año mínimo");
            }
        }else
        {
            Programador pro = (Programador)list.extraerObjeto();
            
            if(pro.cumpleMes()){
                float Sueldo,Resultado;
                Sueldo = pro.getSueldo();
                
                Resultado = Sueldo + Sueldo * pro.getSueldoExtra();
                
                jTFSueldo.setText(Float.toString(Resultado));
            }else
            {
                JFrame frame = new JFrame();
                JOptionPane.showMessageDialog(frame, "Error. El Empleado no cumple el requisito de 1 mes mínimo");
            }
        }
    }//GEN-LAST:event_BotCalcularActionPerformed
   
