    private void ButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonAceptarActionPerformed
        // TODO add your handling code here:
        Empleado emp;
        int bandera = 0;

        try{
            if(jTextNombre.getText() == "" && jTextFecha.getText() == "" && jTextMax.getText() == "" && jTextSueldo.getText() == "")
            throw new ControlException("");
                
            try{

                if(!comprobarSueldo())
                throw new ControlException("");

                float sueldo = Float.parseFloat(jTextSueldo.getText());                    

                try{
                    if(sueldo > Float.parseFloat(jTextMax.getText()))
                        throw new ControlException("");

                    try{

                        if(!comprobarFecha())
                        throw new ControlException("");

                        try{
                            if(!comprobarMax())
                                throw new ControlException("");

                            int max = Integer.parseInt(jTextMax.getText());
                            emp = new Empleado(jTextNombre.getText(), sueldo, max, jTextFecha.getText());

                            lista.crearNodo(emp, emp.getNumero());

                            JFrame frame = new JFrame();
                            JOptionPane.showMessageDialog(frame, "Alta de empleado realizada correctamente.");

                            jTextNombre.setText("");
                            jTextFecha.setText("");
                            jTextSueldo.setText("");
                            jTextMax.setText("");

                        }catch(ControlException e){
                            ControlException ex = new ControlException("int");
                            jTextMax.setText("");
                        }


                    }catch(ControlException e){
                        ControlException ex = new ControlException("fecha");
                        jTextFecha.setText("");
                    }

                }catch(ControlException e){
                    ControlException ex = new ControlException("sueldo");
                    jTextSueldo.setText("");
                }

            }catch(ControlException e)
            {
                ControlException ex = new ControlException("float");
                jTextSueldo.setText("");
            }
        }catch(ControlException e)
        {
            ControlException ex = new ControlException("vacio");
        }
    }//GEN-LAST:event_ButtonAceptarActionPerformed

