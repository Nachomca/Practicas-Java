    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarActionPerformed
        // TODO add your handling code here:

        String nombre = jTextNombre.getText();
        String fecha = jTextFecha.getText();
        int ultimo = 0; //para saber si ha llegado al ultimo

        for(Nodo <Empleado> aux2 = lista.primero(); aux2 != null; aux2 = aux2.getSgt())
        {

            if(aux2.getDato().getNombre().equals(nombre) && aux2.getDato().getFecha().equals(fecha))
            {
                jTextNombre.setVisible(false);
                jTextFecha.setVisible(false);
                jLabelNombre.setVisible(false);
                jLabelFecha.setVisible(false);
                jButtonAceptar.setVisible(false);
                jButtonBorrar.setVisible(false);
                jButtonAceptar2.setVisible(true);
                jTextField1.setVisible(true);
                jComboBox1.setVisible(true);
                aux = aux2;
                break;
            }else if(aux2.getSgt() == null)
            {
                ultimo = 1;
            }

        }

        if(ultimo == 1)
        {
            JFrame frame = new JFrame();
            JOptionPane.showMessageDialog(frame, "Los datos introducidos no son correctos.");
        }
    }//GEN-LAST:event_jButtonAceptarActionPerformed

