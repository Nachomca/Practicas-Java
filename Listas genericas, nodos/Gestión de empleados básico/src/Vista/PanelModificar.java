    private void jButtonAceptar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptar2ActionPerformed
        // TODO add your handling code here:
        switch(op)
        {
            case 1:
            {
                nombre = jTextField1.getText();
                //System.out.println(nombre);
                break;
            }
            case 2:
            {
                try{                   
                    if(!comprobarNumero())
                        throw new ControlException("");
                    
                    sueldoMax = jTextField1.getText();
                    bandera = 0;
                }catch(ControlException e){
                    ControlException ex = new ControlException("int");
                    jTextField1.setText("");
                    bandera = 1;
                }
            }
            case 3:
            {
                try{
                     if(!comprobarFecha())
                        throw new ControlException("");
                     
                    fecha = jTextField1.getText();
                    bandera = 0;
                }catch(ControlException e){
                    ControlException ex = new ControlException("fecha");
                    jTextField1.setText("");
                    bandera = 1;
                }
                
                break;
            }
            case 4:
            {
                try{                   
                    if(!comprobarNumero())
                        throw new ControlException("");
                    
                    sueldo = jTextField1.getText();
                    bandera = 0;
                }catch(ControlException e){
                    ControlException ex = new ControlException("int");
                    jTextField1.setText("");
                    bandera = 1;
                }
                
                break;
            }

        }
        
        if(!nombre.equals(""))
        {
            aux.getDato().setNombre(nombre);
        }
        
        if(!sueldoMax.equals(""))
        {
            aux.getDato().setSueldoMaximo(Integer.parseInt(sueldoMax));
        }
        
        if(!fecha.equals(""))
        {
            try {
                aux.getDato().setFecha(fecha);
            } catch (Exception ex) {
                Logger.getLogger(PanelModificar.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        if(!sueldo.equals(""))
        {
            aux.getDato().setSueldo(Integer.parseInt(sueldo));
        }
        
        if(bandera == 0)
        {
            JFrame frame = new JFrame();
            JOptionPane.showMessageDialog(frame,"Perfil modificado");
        }
    }//GEN-LAST:event_jButtonAceptar2ActionPerformed


